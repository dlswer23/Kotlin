fun main(args: Array<String>){
    var num:Int = 10

    if(num ==0){
        println("num은 0입니다")

    }
    else if(num == 10){
        println("num은 10입니다")

    }
    else{
        println("num은 0과 10이 아닙니다")
    }
}

//결과 값은 num은 10 입니다.

